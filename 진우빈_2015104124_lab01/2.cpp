#include "stdafx.h"
#include <iostream>
using namespace std;

int main() {
	
	int A, B;
	cout << "Please enter two integer values: " << endl;
	cin >> A >> B;
	
	cout << A << " + " << B << " = " << A + B << endl;
	cout << A << " - " << B << " = " << A - B << endl;
	cout << A << " * " << B << " = " << A * B << endl;
	cout << A << " / " << B << " = " << A / B << endl;
	cout << A << " % " << B << " = " << A % B << endl;
	
}